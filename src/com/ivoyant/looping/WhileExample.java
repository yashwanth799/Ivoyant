package com.ivoyant.looping;

public class WhileExample {
    public static void main(String[] args) {

        //Initialized the number to 0
        int num = 0;

        // This loop will run until the number becomes 11
        while (num <= 10) {
            System.out.println(num);
            //Incrementing the value of num
            num++;
        }
    }
}

package com.ivoyant.operators;

public class CompoundExample {
    public static void main(String[] args) {

        int num = 5;

        System.out.println(num += 10);
        System.out.println(num -= 10);
        System.out.println(num *= 10);
        System.out.println(num /= 10);
        System.out.println(num %= 2);

    }
}

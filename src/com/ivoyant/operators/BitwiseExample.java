package com.ivoyant.operators;

public class BitwiseExample {
    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 3;

        System.out.println(num1 & num2);
        System.out.println(num1 | num2);
        System.out.println(num1 ^ 3);
        System.out.println(~num1);
        System.out.println(num1 << 1);
        System.out.println(num1 >> 1);


    }
}

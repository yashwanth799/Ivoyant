package com.ivoyant.datatypes;

public class PrimitiveDataTypesExample {
    public static void main(String[] args) {

        byte age = 25;
        short year = 2024;
        int number = 25000;
        long num = 10000000l;

        float floatValue = 25.50f;
        double doubleValue = 24.56456;

        char character = 'A';
        boolean flag = true;

        System.out.println(age + " " + year + " " + number + " " + num + " " + floatValue + " " + doubleValue + " " + character + " " + flag);
    }

}

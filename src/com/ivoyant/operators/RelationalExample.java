package com.ivoyant.operators;

public class RelationalExample {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        //Outputs are in boolean format
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 != num2);
    }
}
